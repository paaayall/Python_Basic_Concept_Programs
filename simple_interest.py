#Find the Simple Interest on Rs. 200 for 5 years at 5% per year
# Given values
principal = 200  # Principal amount in Rs.
rate = 5         # Rate of interest per year in percent
time = 5         # Time period in years

# Calculate Simple Interest
simple_interest = (principal * rate * time) / 100

# Display the result
print(f"The Simple Interest is: Rs. {simple_interest}")
